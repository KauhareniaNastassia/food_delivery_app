class SignInInfo {
  final String email;
  final String password;

  SignInInfo({
    required this.email,
    required this.password,
  });
}

class SignUpInfo {
  final String userName;
  final String email;
  final String password;

  SignUpInfo({
    required this.userName,
    required this.email,
    required this.password,
  });
}
